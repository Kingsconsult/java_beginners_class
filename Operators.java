public class Operators {
    public static void main(String[] args){

        Integer five = 5;

        int sum = five + five;

        long multi = five * sum;

        double doub = five % 2;

        int plusPlus = ++five;

        

        System.out.println(sum);
        System.out.println(multi);
        System.out.println(doub);
        System.out.println(plusPlus);

    }
}
